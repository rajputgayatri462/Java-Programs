class ForLoop
{
	public static void main(String args[])
	{

// ASCII VALE PRINT 

for(int i = 1 ; i <=127 ; i++){
System.out.println(i + "  " +((char)i));
}

System.out.println();


// Upperscase alphabets

for(char ch = 'Z' ; ch >= 'A' ; ch --){
System.out.print(ch + "  ");
}

System.out.println();

//Lowercase alphabet

for(char ch = 'a' ; ch <= 'z' ; ch ++){
System.out.print(ch + "  ");
}

System.out.println();

//Digits from 0 to 9

for(char ch = '0' ; ch <= '9' ; ch ++){
System.out.print(ch + "  ");
}

System.out.println();

//Numbers from 1 to 10 

for (int i = 1; i<=10 ; i++){
System.out.print(i + " ");
}






	} 
}
